def tag_text(text: str) -> List[str]:
    """对文本内容打标签，返回标签列表（去重）"""
    tags: Set[str] = set()
    text_lower = text.lower()

    for keyword, tag in KEYWORD_TAG_MAP.items():
        if keyword in text_lower:
            tags.add(tag)
            # 额外精确匹配：主体/行业标签直接加入
            if tag in ["主体标签", "行业标签", "城投", "房地产", "医药", "科技"]:
                tags.add(keyword)

    return sorted(tags, key=lambda t: TAG_DISPLAY.get(t, {}).get("priority", 99))


def tag_with_context(text: str) -> Dict[str, any]:
    """带上下文标签分析"""
    tags = tag_text(text)

    # 按类别分组
    grouped = {
        "主体标签": [],
        "业务标签": [],
        "风险标签": [],
        "监管合规": [],
        "行业标签": [],
        "宏观标签": [],
        "资产质量": [],
    }

    for tag in tags:
        if tag in SUBJECT_TAGS or tag in ["银行", "非银金融"]:
            grouped["主体标签"].append(tag)
        elif tag in BUSINESS_TAGS or tag in ["信贷", "授信", "风控"]:
            grouped["业务标签"].append(tag)
        elif tag in RISK_LEVEL_TAGS or tag == "风险":
            grouped["风险标签"].append(tag)
        elif tag in REGULATORY_TAGS or tag in ["合规", "监管", "处罚"]:
            grouped["监管合规"].append(tag)
        elif tag in INDUSTRY_TAGS or tag in ["房地产", "城投", "医药", "科技"]:
            grouped["行业标签"].append(tag)
        elif tag in MACRO_TAGS or tag in ["GDP", "CPI", "LPR", "降息"]:
            grouped["宏观标签"].append(tag)
        elif tag in RISK_LEVEL_TAGS or tag in ["不良", "逾期"]:
            grouped["资产质量"].append(tag)

    # 去重 + 清理空列表
    for k in grouped:
        grouped[k] = list(dict.fromkeys(grouped[k]))  # 保持顺序去重

    return {
        "flat_tags": tags,
        "grouped_tags": {k: v for k, v in grouped.items() if v},
        "tag_count": len(tags),
    }


def suggest_tags_for_article(title: str, content: str) -> Dict[str, any]:
    """为文章推荐标签"""
    combined = f"{title} {content[:3000]}"
    result = tag_with_context(combined)
    result["suggested_tags"] = result["flat_tags"]
    return result


